def add(a, b, third_operand = 0):
    """
    Add some numbers together

    ```py
    add(2, 3) # 5
    add(2, 3, 4) # 9
    ```
    """
    return a + b + third_operand

def sub(a, b, c = 0):
    """
    Subtract some numbers

    ```py
    sub(8, 3) # 5
    sub(8, 3, 2) # 3
    ```
    """
    return a - b - c
